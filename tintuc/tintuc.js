const allArticles = [...tintuc].sort((a, b) => b.id - a.id);
let filteredArticles = [...allArticles];
let toastTimeoutId = null;

function normalizeText(value) {
  return String(value ?? "")
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .trim();
}

function truncateText(value, maxLength = 124) {
  const plain = String(value ?? "").replace(/\s+/g, " ").trim();
  if (plain.length <= maxLength) {
    return plain;
  }
  return `${plain.slice(0, maxLength).trim()}...`;
}

function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#039;");
}

function sanitizeImagePath(path) {
  const cleanPath = String(path ?? "").trim();
  return cleanPath === "" ? ARTICLE_FALLBACK_IMAGE : cleanPath;
}

function getViewCount(articleId) {
  const value = Number(localStorage.getItem(`tintuc_${articleId}_luotxem`));
  return Number.isFinite(value) && value > 0 ? value : 0;
}

function incrementViewCount(articleId) {
  const nextValue = getViewCount(articleId) + 1;
  localStorage.setItem(`tintuc_${articleId}_luotxem`, String(nextValue));
  return nextValue;
}

function resolveFavoriteState(article) {
  const primaryKey = `yeuthich${article.id}`;
  const legacyKey = `yeuthich${article.id - 1}`;
  const primaryValue = localStorage.getItem(primaryKey);

  if (primaryValue === FAVORITE_ACTIVE || primaryValue === FAVORITE_INACTIVE) {
    return primaryValue;
  }

  const legacyValue = localStorage.getItem(legacyKey);
  if (legacyValue === FAVORITE_ACTIVE || legacyValue === FAVORITE_INACTIVE) {
    localStorage.setItem(primaryKey, legacyValue);
    return legacyValue;
  }

  localStorage.setItem(primaryKey, FAVORITE_INACTIVE);
  return FAVORITE_INACTIVE;
}

function isFavorite(article) {
  return resolveFavoriteState(article) === FAVORITE_ACTIVE;
}

function setFavorite(article, active) {
  localStorage.setItem(`yeuthich${article.id}`, active ? FAVORITE_ACTIVE : FAVORITE_INACTIVE);
}

function getFavoriteArticles() {
  return allArticles.filter((article) => isFavorite(article));
}

function getArticleExcerpt(article) {
  if (String(article.pre ?? "").trim() !== "") {
    return truncateText(article.pre, 132);
  }

  return truncateText(
    `Khám phá thêm bài viết "${article.tieude}" cùng những chia sẻ thực tế, hình ảnh minh họa và gợi ý dễ áp dụng từ Thuận Phát Garden.`,
    132
  );
}

function getArticleSearchText(article) {
  return normalizeText(`${article.tieude} ${article.chude} ${article.pre}`);
}

function attachImageFallback(imageElement) {
  if (!imageElement) {
    return;
  }

  imageElement.addEventListener("error", function handleImageError() {
    if (this.src.endsWith("avatar.png")) {
      return;
    }
    this.src = ARTICLE_FALLBACK_IMAGE;
  });
}

function showToast(message) {
  if (!elements.toast) {
    return;
  }

  clearTimeout(toastTimeoutId);
  elements.toast.innerHTML = `<div class="news-toast-card">${escapeHtml(message)}</div>`;
  toastTimeoutId = setTimeout(() => {
    elements.toast.innerHTML = "";
  }, 2200);
}

function updateCounters() {
  const favoriteCount = getFavoriteArticles().length;

  if (elements.articleTotal) {
    elements.articleTotal.textContent = String(allArticles.length);
  }

  if (elements.resultTotal) {
    elements.resultTotal.textContent = String(filteredArticles.length);
  }

  if (elements.favoriteTotal) {
    elements.favoriteTotal.textContent = String(favoriteCount);
  }

  if (elements.favoriteBadge) {
    elements.favoriteBadge.textContent = String(favoriteCount);
  }

  if (elements.favoritesCount) {
    elements.favoritesCount.textContent = String(favoriteCount);
  }
}

function updateSearchState(query) {
  const hasQuery = query !== "";

  if (elements.searchReset) {
    elements.searchReset.hidden = !hasQuery;
  }

  if (elements.sectionTitle) {
    elements.sectionTitle.textContent = hasQuery ? "Kết quả tìm kiếm" : "Bài viết mới và nổi bật";
  }

  if (elements.searchState) {
    elements.searchState.textContent = hasQuery
      ? `Tìm thấy ${filteredArticles.length} bài viết phù hợp với từ khóa "${query}".`
      : "Khám phá những nội dung phù hợp với góc xanh của bạn.";
  }
}

function renderArticleCard(article) {
  const card = document.createElement("article");
  const favoriteActive = isFavorite(article);
  const viewCount = getViewCount(article.id);

  card.className = "news-card";
  card.innerHTML = `
    <div class="news-card__media">
      <a class="news-card__media-link js-article-link" href="${escapeHtml(article.url)}" data-id="${article.id}">
        <img src="${escapeHtml(sanitizeImagePath(article.image))}" alt="${escapeHtml(article.tieude)}" loading="lazy">
      </a>
      <button
        type="button"
        class="news-favorite-button ${favoriteActive ? "is-active" : ""}"
        data-id="${article.id}"
        aria-label="${favoriteActive ? "Bỏ lưu bài viết" : "Lưu bài viết"}"
        aria-pressed="${favoriteActive ? "true" : "false"}">
        <ion-icon name="${favoriteActive ? "heart" : "heart-outline"}"></ion-icon>
      </button>
    </div>
    <div class="news-card__body">
      <span class="news-card__topic">${escapeHtml(article.chude)}</span>
      <h3 class="news-card__title">
        <a class="js-article-link" href="${escapeHtml(article.url)}" data-id="${article.id}">
          ${escapeHtml(article.tieude)}
        </a>
      </h3>
      <p class="news-card__excerpt">${escapeHtml(getArticleExcerpt(article))}</p>
    </div>
    <div class="news-card__footer">
      <span class="news-card__meta">
        <ion-icon name="eye-outline"></ion-icon>
        <span>${viewCount} lượt xem</span>
      </span>
      <a class="news-card__link js-article-link" href="${escapeHtml(article.url)}" data-id="${article.id}">Đọc bài</a>
    </div>
  `;

  attachImageFallback(card.querySelector("img"));
  return card;
}

function renderArticles() {
  if (!elements.list || !elements.emptyState) {
    return;
  }

  elements.list.innerHTML = "";

  if (filteredArticles.length === 0) {
    elements.emptyState.hidden = false;
    updateCounters();
    return;
  }

  elements.emptyState.hidden = true;

  filteredArticles.forEach((article) => {
    elements.list.appendChild(renderArticleCard(article));
  });

  updateCounters();
}

function renderTrending() {
  if (!elements.trending) {
    return;
  }

  const trendingArticles = [...allArticles]
    .sort((a, b) => getViewCount(b.id) - getViewCount(a.id) || b.id - a.id)
    .slice(0, 5);

  elements.trending.innerHTML = "";

  trendingArticles.forEach((article, index) => {
    const item = document.createElement("a");
    const viewCount = getViewCount(article.id);

    item.className = "news-trending-item js-article-link";
    item.href = article.url;
    item.dataset.id = String(article.id);
    item.innerHTML = `
      <span class="news-trending-item__rank">${index + 1}</span>
      <div class="news-trending-item__content">
        <strong>${escapeHtml(article.tieude)}</strong>
        <p>${escapeHtml(article.chude)}</p>
        <span class="news-trending-item__views">
          <ion-icon name="eye-outline"></ion-icon>
          <span>${viewCount} lượt xem</span>
        </span>
      </div>
    `;

    elements.trending.appendChild(item);
  });
}

function renderFavorites() {
  if (!elements.favoritesList) {
    return;
  }

  const favorites = getFavoriteArticles();
  elements.favoritesList.innerHTML = "";

  if (favorites.length === 0) {
    elements.favoritesList.innerHTML = `
      <div class="news-favorites-empty">
        Bạn chưa lưu bài viết nào. Hãy bấm vào biểu tượng trái tim ở danh sách bài viết để lưu nhanh.
      </div>
    `;
    updateCounters();
    return;
  }

  favorites.forEach((article) => {
    const item = document.createElement("article");
    item.className = "news-favorite-item";
    item.innerHTML = `
      <a class="news-favorite-item__image js-article-link" href="${escapeHtml(article.url)}" data-id="${article.id}">
        <img src="${escapeHtml(sanitizeImagePath(article.image))}" alt="${escapeHtml(article.tieude)}" loading="lazy">
      </a>
      <div class="news-favorite-item__body">
        <span>${escapeHtml(article.chude)}</span>
        <a class="js-article-link" href="${escapeHtml(article.url)}" data-id="${article.id}">
          <h3>${escapeHtml(article.tieude)}</h3>
        </a>
        <p>${escapeHtml(getArticleExcerpt(article))}</p>
        <div class="news-favorite-item__footer">
          <span class="news-favorite-item__views">
            <ion-icon name="eye-outline"></ion-icon>
            <span>${getViewCount(article.id)} lượt xem</span>
          </span>
          <button type="button" class="news-favorite-remove" data-id="${article.id}">
            <ion-icon name="trash-outline"></ion-icon>
            <span>Bỏ lưu</span>
          </button>
        </div>
      </div>
    `;

    attachImageFallback(item.querySelector("img"));
    elements.favoritesList.appendChild(item);
  });

  updateCounters();
}

function rerenderAll() {
  renderArticles();
  renderTrending();
  renderFavorites();
}

function applySearch(query) {
  const normalizedQuery = normalizeText(query);

  filteredArticles = normalizedQuery
    ? allArticles.filter((article) => getArticleSearchText(article).includes(normalizedQuery))
    : [...allArticles];

  updateSearchState(String(query ?? "").trim());
  renderArticles();
}

function openFavoritesDrawer() {
  if (!elements.favoritesDrawer || !elements.favoritesOverlay) {
    return;
  }

  elements.favoritesDrawer.classList.add("is-open");
  elements.favoritesDrawer.setAttribute("aria-hidden", "false");
  elements.favoritesOverlay.hidden = false;
  document.body.classList.add("news-drawer-open");
}

function closeFavoritesDrawer() {
  if (!elements.favoritesDrawer || !elements.favoritesOverlay) {
    return;
  }

  elements.favoritesDrawer.classList.remove("is-open");
  elements.favoritesDrawer.setAttribute("aria-hidden", "true");
  elements.favoritesOverlay.hidden = true;
  document.body.classList.remove("news-drawer-open");
}

function toggleFavoriteById(articleId) {
  const article = allArticles.find((item) => item.id === Number(articleId));
  if (!article) {
    return;
  }

  const nextState = !isFavorite(article);
  setFavorite(article, nextState);
  rerenderAll();
  showToast(nextState ? "Đã lưu bài viết vào danh sách yêu thích." : "Đã bỏ bài viết khỏi danh sách yêu thích.");
}

function clearFavorites() {
  const favorites = getFavoriteArticles();
  favorites.forEach((article) => setFavorite(article, false));
  rerenderAll();
  showToast("Đã xóa toàn bộ bài viết yêu thích.");
}

function handleArticleNavigation(link, articleId, event) {
  if (!link || !articleId) {
    return;
  }

  if (event.defaultPrevented) {
    return;
  }

  if (event.button !== 0 || event.metaKey || event.ctrlKey || event.shiftKey || event.altKey) {
    incrementViewCount(articleId);
    return;
  }

  event.preventDefault();
  incrementViewCount(articleId);
  rerenderAll();
  window.location.href = link.href;
}

function bindEvents() {
  if (elements.searchForm && elements.searchInput) {
    elements.searchForm.addEventListener("submit", (event) => {
      event.preventDefault();
      applySearch(elements.searchInput.value);
    });

    elements.searchInput.addEventListener("input", () => {
      if (elements.searchInput.value.trim() === "") {
        applySearch("");
      }
    });
  }

  if (elements.searchReset && elements.searchInput) {
    elements.searchReset.addEventListener("click", () => {
      elements.searchInput.value = "";
      applySearch("");
      elements.searchInput.focus();
    });
  }

  if (elements.favoritesOpen) {
    elements.favoritesOpen.addEventListener("click", openFavoritesDrawer);
  }

  if (elements.favoritesClose) {
    elements.favoritesClose.addEventListener("click", closeFavoritesDrawer);
  }

  if (elements.favoritesOverlay) {
    elements.favoritesOverlay.addEventListener("click", closeFavoritesDrawer);
  }

  if (elements.favoritesClear) {
    elements.favoritesClear.addEventListener("click", clearFavorites);
  }

  document.addEventListener("click", (event) => {
    const favoriteButton = event.target.closest(".news-favorite-button");
    const removeButton = event.target.closest(".news-favorite-remove");
    const articleLink = event.target.closest(".js-article-link");

    if (favoriteButton) {
      event.preventDefault();
      toggleFavoriteById(Number(favoriteButton.dataset.id));
      return;
    }

    if (removeButton) {
      event.preventDefault();
      toggleFavoriteById(Number(removeButton.dataset.id));
      return;
    }

    if (articleLink) {
      handleArticleNavigation(articleLink, Number(articleLink.dataset.id), event);
    }
  });

  document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
      closeFavoritesDrawer();
    }
  });
}

bindEvents();
updateSearchState("");
rerenderAll();
